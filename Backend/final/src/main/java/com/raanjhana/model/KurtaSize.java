package com.raanjhana.model;

public class KurtaSize {
    
    private String phoneNumber;
    private Double length, chest, gap, waist, hips, shoulder, sleeve, bicep, elbow, cuff, cb, neck;

    public KurtaSize() {
    }

    public String getPhoneNumber() {
        return phoneNumber;
    }

    public void setPhoneNumber(String phoneNumber) {
        this.phoneNumber = phoneNumber;
    }

    public Double getLength() {
        return length;
    }

    public void setLength(Double length) {
        this.length = length;
    }

    public Double getChest() {
        return chest;
    }

    public void setChest(Double chest) {
        this.chest = chest;
    }

    public Double getGap() {
        return gap;
    }

    public void setGap(Double gap) {
        this.gap = gap;
    }

    public Double getWaist() {
        return waist;
    }

    public void setWaist(Double waist) {
        this.waist = waist;
    }

    public Double getHips() {
        return hips;
    }

    public void setHips(Double hips) {
        this.hips = hips;
    }

    public Double getShoulder() {
        return shoulder;
    }

    public void setShoulder(Double shoulder) {
        this.shoulder = shoulder;
    }

    public Double getSleeve() {
        return sleeve;
    }

    public void setSleeve(Double sleeve) {
        this.sleeve = sleeve;
    }

    public Double getBicep() {
        return bicep;
    }

    public void setBicep(Double bicep) {
        this.bicep = bicep;
    }

    public Double getElbow() {
        return elbow;
    }

    public void setElbow(Double elbow) {
        this.elbow = elbow;
    }

    public Double getCuff() {
        return cuff;
    }

    public void setCuff(Double cuff) {
        this.cuff = cuff;
    }

    public Double getCb() {
        return cb;
    }

    public void setCb(Double cb) {
        this.cb = cb;
    }

    public Double getNeck() {
        return neck;
    }

    public void setNeck(Double neck) {
        this.neck = neck;
    }
    

}
