package com.raanjhana.model;

public class TrouserSize {
    
    private String phoneNumber;
    private String pleats;
    private Double length;
    private Double waist;
    private Double il;
    private Double hips;
    private Double thigh;
    private Double r;
    private Double knee;
    private Double calf;
    private Double bottom;

    
    public TrouserSize() {
    }

    public String getPhoneNumber() {
        return phoneNumber;
    }


    public void setPhoneNumber(String phoneNumber) {
        this.phoneNumber = phoneNumber;
    }


    public String getPleats() {
        return pleats;
    }


    public void setPleats(String pleats) {
        this.pleats = pleats;
    }


    public Double getLength() {
        return length;
    }


    public void setLength(Double length) {
        this.length = length;
    }


    public Double getWaist() {
        return waist;
    }


    public void setWaist(Double waist) {
        this.waist = waist;
    }


    public Double getIl() {
        return il;
    }


    public void setIl(Double il) {
        this.il = il;
    }


    public Double getHips() {
        return hips;
    }


    public void setHips(Double hips) {
        this.hips = hips;
    }


    public Double getThigh() {
        return thigh;
    }


    public void setThigh(Double thigh) {
        this.thigh = thigh;
    }


    public Double getR() {
        return r;
    }


    public void setR(Double r) {
        this.r = r;
    }


    public Double getKnee() {
        return knee;
    }


    public void setKnee(Double knee) {
        this.knee = knee;
    }


    public Double getCalf() {
        return calf;
    }


    public void setCalf(Double calf) {
        this.calf = calf;
    }


    public Double getBottom() {
        return bottom;
    }


    public void setBottom(Double bottom) {
        this.bottom = bottom;
    }

}
